package com.ithiema.content;

public class Tactics {

	private String id;
	private String tname;
	private String type;
	private String tcontent;
	private String tadv;
	private String tdef;
	private String ex;
	
	public void setId(String id) {
		this.id=id;
	}
	public String getId() {
		return id;
	}
	public void setTname(String tname) {
		this.tname=tname;
	}
	public String getTname() {
		return tname;
	}
	public void setType(String type) {
		this.type=type;
	}
	public String getType() {
		return type;
	}
	public void setTcontent(String tcontent) {
		this.tcontent=tcontent;
	}
	public String getTcontent() {
		return tcontent;
	}
	public void setTadv(String tadv) {
		this.tadv=tadv;
	}
	public String getTadv() {
		return tadv;
	}
	public void setTdef(String tdef) {
		this.tdef=tdef;
	}
	public String getTdef() {
		return tdef;
	}
	public void setEx(String ex) {
		this.ex=ex;
	}
	public String getEx() {
		return ex;
	}
	
	
}
